#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	int	i;

	i = 0;
	while (s[i] != '\0' && i < n)
	{
		if(s[i] == (char)c)
			return (s[i]);
		i++;
	}
	return (NULL);
}
